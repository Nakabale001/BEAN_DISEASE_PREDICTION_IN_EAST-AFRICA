<?php

require_once "config.php";

if (!isset($_SESSION['prediction'])) {

    die("No prediction data available.");

}

require_once "fpdf/fpdf.php";

$data = $_SESSION['prediction'];

$disease = $data['disease'];
$confidence = $data['confidence'];
$image = $data['image'];
$lang = $data['lang'];
$date = $data['prediction_date'];

if (!file_exists("languages/$lang.php")) {

    $lang = "en";

}

include "languages/$lang.php";

$diseaseName =
    $disease_names[$disease]
    ?? formatDiseaseName($disease);

$cause =
    $disease_causes[$disease]
    ?? "Information not available.";

$prevention =
    $disease_prevention[$disease]
    ?? "Information not available.";

$advice =
    $disease_advice[$disease]
    ?? "Information not available.";

// =====================================
// CREATE PDF
// =====================================

$pdf = new FPDF();

$pdf->AddPage();

$pdf->SetTitle(
    "Bean Leaf Disease Prediction Report"
);

$pdf->SetAuthor(
    SYSTEM_TITLE
);

// =====================================
// HEADER
// =====================================

$pdf->SetFont(
    'Arial',
    'B',
    16
);

$pdf->MultiCell(
    0,
    10,
    SYSTEM_TITLE,
    0,
    'C'
);

$pdf->Ln(3);

$pdf->SetFont(
    'Arial',
    '',
    12
);

$pdf->Cell(
    0,
    8,
    'Bean Leaf Disease Prediction Report',
    0,
    1,
    'C'
);

$pdf->Ln(5);

// =====================================
// IMAGE
// =====================================

$imagePath = __DIR__ . DIRECTORY_SEPARATOR . $image;

if (file_exists($imagePath)) {

    $pdf->Image(
        $imagePath,
        70,
        45,
        70
    );

    $pdf->Ln(75);

}

// =====================================
// PREDICTION RESULTS
// =====================================

$pdf->SetFont(
    'Arial',
    'B',
    12
);

$pdf->Cell(
    50,
    10,
    'Predicted Disease:'
);

$pdf->SetFont(
    'Arial',
    '',
    12
);

$pdf->Cell(
    100,
    10,
    $diseaseName,
    0,
    1
);

$pdf->SetFont(
    'Arial',
    'B',
    12
);

$pdf->Cell(
    50,
    10,
    'Confidence Score:'
);

$pdf->SetFont(
    'Arial',
    '',
    12
);

$pdf->Cell(
    100,
    10,
    $confidence . '%',
    0,
    1
);

$pdf->SetFont(
    'Arial',
    'B',
    12
);

$pdf->Cell(
    50,
    10,
    'Prediction Date:'
);

$pdf->SetFont(
    'Arial',
    '',
    12
);

$pdf->Cell(
    100,
    10,
    $date,
    0,
    1
);

$pdf->Ln(5);

// =====================================
// CAUSE
// =====================================

$pdf->SetFont(
    'Arial',
    'B',
    12
);

$pdf->Cell(
    0,
    10,
    'Disease Cause',
    0,
    1
);

$pdf->SetFont(
    'Arial',
    '',
    11
);

$pdf->MultiCell(
    0,
    8,
    $cause
);

$pdf->Ln(3);

// =====================================
// PREVENTION
// =====================================

$pdf->SetFont(
    'Arial',
    'B',
    12
);

$pdf->Cell(
    0,
    10,
    'Prevention Measures',
    0,
    1
);

$pdf->SetFont(
    'Arial',
    '',
    11
);

$pdf->MultiCell(
    0,
    8,
    $prevention
);

$pdf->Ln(3);

// =====================================
// RECOMMENDATIONS
// =====================================

$pdf->SetFont(
    'Arial',
    'B',
    12
);

$pdf->Cell(
    0,
    10,
    'Recommended Action',
    0,
    1
);

$pdf->SetFont(
    'Arial',
    '',
    11
);

$pdf->MultiCell(
    0,
    8,
    $advice
);

$pdf->Ln(10);

// =====================================
// FOOTER
// =====================================

$pdf->SetFont(
    'Arial',
    'I',
    10
);

$pdf->Cell(
    0,
    10,
    'Generated by ' . SYSTEM_TITLE,
    0,
    1,
    'C'
);

// =====================================
// DOWNLOAD PDF
// =====================================

$pdf->Output(
    'D',
    'Bean_Leaf_Disease_Report.pdf'
);

exit();

?>